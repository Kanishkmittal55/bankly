    private void jSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSubmitButtonActionPerformed
        try {
            
               
            if("".equals(jFirstNameField.getText()) ||  "".equals(jSurNameField.getText()) ){
                jStatusMessageLabel.setText("Please Enter you First Name and Surname to continue");
                JOptionPane.showMessageDialog(this, "Please Enter you First Name and Surname to continue");   
            }
            else if (jDesignationComboBox.getSelectedItem().equals("Select your Designation") ){
                jStatusMessageLabel.setText("You must specify Role/Designation to continue...");
                JOptionPane.showMessageDialog(this, "You must specify Role/Designation to continue...");  
            }
            else if ("".equals(jEmailField.getText()) || !jEmailField.getText().contains("@")){
                jStatusMessageLabel.setText("Please Enter a valid email to continue...");
                JOptionPane.showMessageDialog(this, "Please Enter a valid email to continue...");
            }
            else if ("".equals(jContactNumberField.getText())){
                jStatusMessageLabel.setText("Please Enter your contact number to continue");
                JOptionPane.showMessageDialog(this, "Please Enter your contact number to continue...");
            }
            else if (!jPasswordField.getText().equals(jRepeatPasswordField.getText())){
                jStatusMessageLabel.setText("Passwords do not match");
                JOptionPane.showMessageDialog(this, "Passwords do not match, please chek=ck them...");
            }
            else if(theUser.isRegistered(true,jFirstNameField.getText(),jSurNameField.getText(),jDesignationComboBox.getSelectedItem().toString(),jEmailField.getText(),jContactNumberField.getText(),jPasswordField.getText(),jRepeatPasswordField.getText())){
                jStatusMessageLabel.setText("User Registered Successfully.");
                jAfterRegisterLoginButton.setVisible(true);
                jSubmitButton.setEnabled(false);
                JOptionPane.showMessageDialog(this, "User Registered Successfully");
            }
            
            
            } catch (IOException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jSubmitButtonActionPerformed

